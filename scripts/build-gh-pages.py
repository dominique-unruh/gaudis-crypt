def extract_error_excerpt(build_log: str) -> str:
    """Extract a small tail excerpt from the build log for reporting."""
    # Take the tail-ish part of the log (most useful), but keep it small.
    lines = build_log.splitlines()
    if not lines:
        return ""
    tail = lines[-80:]
    return "\n".join(tail)


