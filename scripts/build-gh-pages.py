"""Build doc-gen4 docs locally and publish to gh-pages.

Design goals (per repo request):
- Manual website sources live on main branch under `site/`.
- Autogenerated Lean docs are produced by doc-gen4.
- The result (manual + autogenerated) is written into the `gh-pages` branch.
- If docs build fails, keep previous gh-pages output as a fallback (stale),
  but inject a prominent disclaimer into the generated HTML.
- If docs build succeeds, recreate gh-pages content from scratch.

Usage:
  python3 scripts/build-gh-pages.py
  python3 scripts/build-gh-pages.py --push

Notes:
- This script uses `git worktree` to avoid clobbering your current checkout.
- It expects to run from anywhere inside the git repo.
"""

from __future__ import annotations

