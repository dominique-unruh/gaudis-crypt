STALE_BANNER_START = "<!-- STALE_BUILD_BANNER_START -->"
STALE_BANNER_END = "<!-- STALE_BUILD_BANNER_END -->"

# Set by --verbose.
VERBOSE = False

# Where to redirect missing dependency module pages.
# mathlib4_docs hosts docs for Mathlib + core libs (Init/Std/Lean/Lake) + common packages.
MATHLIB_DOCS_BASE = "https://leanprover-community.github.io/mathlib4_docs/"


def run(
    cmd: list[str],
    *,
    cwd: Path | None = None,
    check: bool = True,
    verbose: bool = False,
) -> subprocess.CompletedProcess:
    """Run a subprocess command and capture combined stdout/stderr.

    If `--verbose` is enabled (global `VERBOSE`) or `verbose=True` is passed, the
    executed command is printed (with cwd).

    This helper is used for all git/lake invocations so that:
    - output can be included in build logs
    - failures can be handled explicitly (`check=False`)
    """
    if verbose or VERBOSE:
        where = f" (cwd={cwd})" if cwd else ""
        print("$ " + " ".join(cmd) + where)
    return subprocess.run(
        cmd,
        cwd=str(cwd) if cwd else None,
        check=check,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
    )


def repo_root() -> Path:
    """Return the git repository root directory."""
    p = run(["git", "rev-parse", "--show-toplevel"]).stdout.strip()
    return Path(p)


def detect_project_root_module(root: Path) -> str:
    """Pick the main project lean_lib name from lakefile.toml.

    Heuristic: return the first `[[lean_lib]] name = "..."` that is not DocEntrypoint.
    """
    # We keep this simple: the lakefile already declares the project lean libs.
    # If the repo adds more lean_libs later, we still pick the first non-doc-entrypoint.
    text = read_text(root / "lakefile.toml")
    m = re.findall(r"\[\[lean_lib\]\]\s*\n\s*name\s*=\s*\"([^\"]+)\"", text)
    for name in m:
        if name != "DocEntrypoint":
            return name
    return "GaudisCrypt"


def module_name_from_html_path(project_root: str, rel: Path) -> str | None:
    """Convert `GaudisCrypt/Foo/Bar.html` -> `GaudisCrypt.Foo.Bar`."""
    # Used to map doc-gen4 HTML file paths back to Lean module names.
    if rel.suffix != ".html":
        return None
    parts = list(rel.with_suffix("").parts)
    if not parts:
        return None
    if parts[0] != project_root:
        return None
    return ".".join(parts)


def module_to_rel_html(project_root: str, mod: str) -> Path:
    """Map a module name to a doc-gen4 relative HTML path.

    Examples:
    - `GaudisCrypt` -> `GaudisCrypt.html`
    - `GaudisCrypt.Language.Modules` -> `GaudisCrypt/Language/Modules.html`
    """
    parts = mod.split(".")
    if mod == project_root:
        return Path(f"{project_root}.html")
    return Path(*parts).with_suffix(".html")


def generate_project_navbar(doc_dir: Path, project_root: str, modules: list[str]) -> str:
    """Generate a navbar.html that links only to project theories.

    This intentionally omits the "General documentation" section.
    The view is hierarchical: `GaudisCrypt` contains `Lib`, `Logic`, etc.
    """

    # Note: We generate a navbar.html that is "close enough" to doc-gen4's navbar
    # so that the existing CSS (style.css) continues to work.
    #
    # Important detail: for excluded modules we might not have a local html file.
    # In that case we render them as non-clickable leaves.

    # Build a module tree from module name components.
    class Node:
        """A node in the module hierarchy tree."""
        __slots__ = ("name", "children", "mod")

        def __init__(self, name: str):
            self.name = name
            self.children: dict[str, Node] = {}
            self.mod: str | None = None

    root_node = Node(project_root)

    # Ensure stable order and uniqueness.
    seen: set[str] = set()
    ordered = []
    for m in modules:
        if not m.startswith(project_root):
            continue
        if m in seen:
            continue
        seen.add(m)
        ordered.append(m)
    ordered.sort()

    # Insert each module into the tree.
    for mod in ordered:
        parts = mod.split(".")
        if not parts or parts[0] != project_root:
            continue
        cur = root_node
        for p in parts[1:]:
            cur = cur.children.setdefault(p, Node(p))
        cur.mod = mod

    def link_for(mod: str) -> str | None:
        """Return a local href for `mod` if the corresponding HTML exists."""
        rel = module_to_rel_html(project_root, mod)
        if (doc_dir / rel).exists():
            return "./" + rel.as_posix()
        return None

    def render_node(node: Node, prefix_path: str) -> str:
        """Render the children of `node` (root-level expansion helper)."""
        # prefix_path is like "./GaudisCrypt/Lib.html" for data-path hints.
        # This matches doc-gen4's general structure enough to keep CSS working.
        parts: list[str] = []
        # Root: render its children, no outer <details> wrapper.
        for key in sorted(node.children.keys()):
            child = node.children[key]
            # Determine module name for this node if it corresponds to a module.
            # E.g. GaudisCrypt.Lib corresponds to module GaudisCrypt.Lib (rare), but typically
            # only leaves exist. We still allow a file link if an html exists.
            # Compute full module name by walking from root in recursion.
            # We'll pass it down as prefix_path only for data-path; actual file link is computed.

            rendered = render_subtree(child, parent_mod=(project_root + ("." + child.name if child.name else "")))
            parts.append(rendered)
        return "".join(parts)

    def render_subtree(node: Node, parent_mod: str) -> str:
        """Render a subtree as nested <details> sections and leaf links."""
        # parent_mod is the module name at this node (even if there is no file).
        file_link = link_for(parent_mod)

        if node.children:
            # Directory node.
            # Summary shows short name, optionally with a file link.
            if file_link:
                summary = f'{escape_html(node.name)} (<a href="{file_link}">file</a>)'
            else:
                summary = escape_html(node.name)
            inner = []
            # If this node is also a module leaf and has a file, add it as a link entry.
            if node.mod and file_link:
                inner.append(f'<div class="nav_link"><a class="break_within" href="{file_link}">{escape_html(node.name)}</a></div>')
            for key in sorted(node.children.keys()):
                child = node.children[key]
                inner.append(render_subtree(child, parent_mod=parent_mod + "." + child.name))
            return f'<details class="nav_sect" data-path="{file_link or ""}"><summary>{summary}</summary>{"".join(inner)}</details>'

        # Leaf node.
        href = link_for(node.mod or parent_mod)
        label = escape_html(node.name)
        if href:
            return f'<div class="nav_link"><a class="break_within" href="{href}">{label}</a></div>'
        else:
            return f'<div class="nav_link"><span class="break_within">{label}</span></div>'

    # Render root as a single open <details> so it's nicely grouped.
    root_link = link_for(project_root)
    root_summary = escape_html(project_root)
    if root_link:
        root_summary = f'{escape_html(project_root)} (<a href="{root_link}">file</a>)'

    body = (
        '<div class="navframe"><nav class="nav">'
        '<div class="module_list">'
        f'<details class="nav_sect" open="true" data-path="{root_link or ""}"><summary>{root_summary}</summary>'
        + render_node(root_node, "")
        + '</details>'
        '</div></nav></div>'
    )

    return (
        '<html lang="en"><head>'
        '<meta charset="UTF-8"></meta>'
        '<meta name="viewport" content="width=device-width, initial-scale=1"></meta>'
        '<link rel="stylesheet" href="./style.css"></link>'
        '<link rel="icon" href="./favicon.svg"></link>'
        '<link rel="mask-icon" href="./favicon.svg" color="#000000"></link>'
        '<link rel="prefetch" href=".//declarations/declaration-data.bmp" as="image"></link>'
        '<script type="module" src="./nav.js"></script>'
        '<script type="module" src="./color-scheme.js"></script>'
        '<base target="_parent"></base>'
        '</head><body>'
        + body
        + '</body></html>'
    )


def prune_nonproject_modules(doc_dir: Path, project_root: str) -> None:
    """Delete generated module pages/dirs not belonging to our project."""
    # After doc-gen4 generates docs, the `doc/` directory contains many dependency
    # pages (Mathlib, Std, Lean, etc.). We remove them so that the published docs
    # only contain our project.
    keep_root_files = {
        "index.html",
        "404.html",
        "navbar.html",
        "search.html",
        "foundational_types.html",
        "tactics.html",
        "references.html",
        "references.bib",
    }
    keep_dirs = {
        project_root,
        "find",
        "declarations",
        # some doc-gen4 versions may have this
        "src",
    }

    for child in doc_dir.iterdir():
        if child.is_dir() and not child.is_symlink():
            if child.name in keep_dirs:
                continue
            shutil.rmtree(child)
            continue
        if child.is_file() and child.suffix.lower() == ".html":
            if child.name in keep_root_files:
                continue
            child.unlink(missing_ok=True)


def rewrite_broken_links_to_online_docs(docs_root: Path) -> None:
    """Rewrite links to missing local .html pages to mathlib4_docs.

    This is intended to run after pruning non-project docs. At that point links to
    dependency modules (Mathlib/Std/Lean/...) would be broken locally; we rewrite
    them to the public documentation site.
    """

    # Strategy:
    # - Scan every HTML file we publish.
    # - For each href="..." attribute, if it points to a local .html page that no
    #   longer exists (because we pruned it), rewrite it to mathlib4_docs.
    # - Preserve any fragment (#...) and query string (?...) suffix.

    # Regex is intentionally simple: doc-gen4 emits plain href="..." attributes.
    href_re = re.compile(r"href=\"([^\"]+)\"")

    scanned_files = 0
    seen_links = 0
    rewritten = 0
    rewritten_targets: dict[str, int] = {}

    for html_path in iter_html_files(docs_root):
        scanned_files += 1
        html = read_text(html_path)
        changed = False

        def replace(m: re.Match[str]) -> str:
            nonlocal seen_links, rewritten, changed
            href = m.group(1)
            seen_links += 1

            # Skip external links and fragment-only links.
            if href.startswith("http://") or href.startswith("https://"):
                return m.group(0)
            if href.startswith("#"):
                return m.group(0)
            if href.startswith("mailto:") or href.startswith("javascript:"):
                return m.group(0)

            # Split off query/fragment.
            path_part = href
            suffix = ""
            for sep in ["#", "?"]:
                if sep in path_part:
                    base, rest = path_part.split(sep, 1)
                    path_part = base
                    suffix = sep + rest + suffix

            if not path_part.endswith(".html"):
                return m.group(0)

            # Resolve the target relative to the current HTML file.
            # Example: in `GaudisCrypt/Language/Modules.html`, a link like
            # `../.././Mathlib/.../AList.html` becomes a filesystem path under docs_root.
            base_dir = html_path.parent
            target_fs = (base_dir / path_part).resolve()

            try:
                rel_to_docs = target_fs.relative_to(docs_root.resolve())
            except Exception:
                # Target points outside docs_root; don't rewrite.
                return m.group(0)

            # If it exists locally, keep.
            if target_fs.exists():
                return m.group(0)

            # Rewrite missing local target to mathlib4_docs.
            new_href = MATHLIB_DOCS_BASE + rel_to_docs.as_posix() + suffix
            rewritten += 1
            changed = True
            rewritten_targets[str(rel_to_docs)] = rewritten_targets.get(str(rel_to_docs), 0) + 1
            return f'href="{new_href}"'

        new_html = href_re.sub(replace, html)
        if changed and new_html != html:
            write_text(html_path, new_html)

    if VERBOSE:
        print(f"link rewrite: scanned {scanned_files} html files")
        print(f"link rewrite: saw {seen_links} href attributes")
        print(f"link rewrite: rewrote {rewritten} links to {MATHLIB_DOCS_BASE}")
        if rewritten_targets:
            top = sorted(rewritten_targets.items(), key=lambda kv: kv[1], reverse=True)[:10]
            print("link rewrite: top rewritten targets:")
            for k, v in top:
                print(f"  {k}  ({v})")


def postprocess_doc_tree(doc_dir: Path, project_root: str) -> None:
    """Restrict docs nav + delete non-project module pages."""
    # This is called right after copying doc-gen4 output into the publish directory.
    # It removes dependency pages, but does NOT yet write navbar.html (we do that
    # after restoring excluded theories, so the navbar can include them).
    prune_nonproject_modules(doc_dir, project_root)
    # Navbar is generated later, after excluded theories are restored.


def build_navbar_module_list(project_root: str, *, generated_relpaths: set[Path], excluded: set[str]) -> list[str]:
    """Compute the set of project modules that should appear in the navbar.

    - All generated project modules (from manifest)
    - Plus all excluded modules (even if their HTML came from stale gh-pages)
    """
    # Note: generated_relpaths contains *all* doc-gen4 outputs. We only include
    # those under the project root.
    mods: set[str] = set()
    for rel in generated_relpaths:
        m = module_name_from_html_path(project_root, rel)
        if m:
            mods.add(m)
    mods |= set(excluded)
    return sorted(mods)


def current_head_sha(root: Path) -> str:
    """Return the current git HEAD SHA."""
    return run(["git", "rev-parse", "HEAD"], cwd=root).stdout.strip()


def git_branch_exists(root: Path, branch: str) -> bool:
    """Return True if the given local git branch exists."""
    cp = run(["git", "show-ref", "--verify", f"refs/heads/{branch}"], cwd=root, check=False)
    return cp.returncode == 0


def git_remote_branch_exists(root: Path, remote: str, branch: str) -> bool:
    """Return True if `remote` has `branch`, even if it isn't fetched locally."""
    cp = run(["git", "ls-remote", "--heads", remote, branch], cwd=root, check=False)
    return cp.returncode == 0 and bool(cp.stdout.strip())


def ensure_worktree(root: Path, branch: str, worktree_dir: Path) -> None:
    """Ensure `worktree_dir` is a git worktree checked out at `branch`.

    The script uses worktrees so we can update `gh-pages` without touching the
    current working directory.
    """
    # Clean up any stale registration first (e.g. previous crash).
    run(["git", "worktree", "prune"], cwd=root, check=False)
    if worktree_dir.exists():
        # If it already exists, it might be a stale checkout. Remove and recreate.
        run(["git", "worktree", "remove", "--force", str(worktree_dir)], cwd=root, check=False)
        shutil.rmtree(worktree_dir, ignore_errors=True)
    worktree_dir.parent.mkdir(parents=True, exist_ok=True)

    # If the branch already has an attached worktree, `git worktree add` fails.
    # We proactively remove it to make the script idempotent.
    cp = run(["git", "worktree", "list", "--porcelain"], cwd=root, check=False)
    if cp.returncode == 0:
        lines = cp.stdout.splitlines()
        cur_path = None
        cur_branch = None
        for line in lines + [""]:
            if line.startswith("worktree "):
                cur_path = line[len("worktree ") :].strip()
                cur_branch = None
            elif line.startswith("branch "):
                cur_branch = line[len("branch ") :].strip()
            elif line == "":
                if cur_path and cur_branch == f"refs/heads/{branch}":
                    # Don't remove the main checkout.
                    if Path(cur_path).resolve() != root.resolve():
                        run(["git", "worktree", "remove", "--force", cur_path], cwd=root, check=False)
                cur_path = None
                cur_branch = None

    if git_branch_exists(root, branch):
        run(["git", "worktree", "add", str(worktree_dir), branch], cwd=root)
        return

    # If the remote branch exists but the user didn't fetch it, creating a new
    # local branch off HEAD would later make `git push origin <branch>` fail
    # (non-fast-forward). Detect via ls-remote and fetch only that branch.
    remote = "origin"
    if git_remote_branch_exists(root, remote, branch):
        # Create the remote-tracking ref without fetching other branches.
        run(["git", "fetch", remote, f"{branch}:refs/remotes/{remote}/{branch}"], cwd=root)
        run(["git", "worktree", "add", "-b", branch, str(worktree_dir), f"{remote}/{branch}"], cwd=root)
        return

    # Remote doesn't have the branch (or no remote configured): create it.
    # The script will wipe the worktree before writing gh-pages content.
    run(["git", "worktree", "add", "-b", branch, str(worktree_dir), "HEAD"], cwd=root)



def remove_all_except_git(worktree_root: Path, *, preserve: set[str] | None = None) -> None:
    """Delete everything in the worktree except `.git` and preserved names."""
    preserve = preserve or set()
    for child in worktree_root.iterdir():
        if child.name == ".git":
            continue
        if child.name in preserve:
            continue
        if child.is_dir() and not child.is_symlink():
            shutil.rmtree(child)
        else:
            child.unlink(missing_ok=True)


def copy_tree(src: Path, dst: Path) -> None:
    """Recursively copy `src` into `dst` (like `cp -R`)."""
    if not src.exists():
        return
    dst.mkdir(parents=True, exist_ok=True)
    for root, dirs, files in os.walk(src):
        r = Path(root)
        rel = r.relative_to(src)
        (dst / rel).mkdir(parents=True, exist_ok=True)
        for d in dirs:
            (dst / rel / d).mkdir(parents=True, exist_ok=True)
        for f in files:
            s = r / f
            t = dst / rel / f
            shutil.copy2(s, t)


def run_jekyll_build(site_src: Path, site_dst: Path) -> None:
    """Build the Jekyll site locally into `site_dst`.

    Uses Bundler if `Gemfile` exists; otherwise tries `jekyll` directly.
    """
    if (site_src / "Gemfile").exists():
        # Ensure Gemfile.lock is consistent; keep it local to the repo.
        run(["bundle", "install"], cwd=site_src)
        run(
            ["bundle", "exec", "jekyll", "build", "--source", str(site_src), "--destination", str(site_dst)],
            cwd=site_src,
        )
    else:
        run(["jekyll", "build", "--source", str(site_src), "--destination", str(site_dst)], cwd=site_src)


def read_text(path: Path) -> str:
    """Read a file as UTF-8, replacing errors."""
    return path.read_text(encoding="utf-8", errors="replace")


def write_text(path: Path, content: str) -> None:
    """Write a UTF-8 file, creating parent directories."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def strip_stale_banner(html: str) -> str:
    """Remove our injected banner block (if present)."""
    if STALE_BANNER_START not in html:
        return html
    # Remove any existing banner block.
    pattern = re.compile(re.escape(STALE_BANNER_START) + r".*?" + re.escape(STALE_BANNER_END) + r"\s*", re.DOTALL)
    return re.sub(pattern, "", html)


def inject_stale_banner(html: str, banner_html: str) -> str:
    """Insert a banner into the HTML.

    Prefer inserting into `<main>` (first child) because doc-gen pages use `<main>`
    for the page content; falling back to `<body>` keeps it robust.
    """
    html = strip_stale_banner(html)

    # Prefer inserting into <main> as the first child.
    m = re.search(r"<main[^>]*>", html, flags=re.IGNORECASE)
    if m:
        insert_at = m.end()
        return html[:insert_at] + "\n" + banner_html + html[insert_at:]

    # Fallback: insert right after the opening <body ...> tag.
    if "<body" not in html:
        return banner_html + "\n" + html
    m = re.search(r"<body[^>]*>", html, flags=re.IGNORECASE)
    if not m:
        return banner_html + "\n" + html
    insert_at = m.end()
    return html[:insert_at] + "\n" + banner_html + html[insert_at:]


def banner_block(*, head_sha: str, build_time_utc: str, error_excerpt: str) -> str:
    """Build a detailed (build-level) banner. (Not currently used for site index.)"""
    safe_excerpt = (error_excerpt or "").strip()
    if len(safe_excerpt) > 2000:
        safe_excerpt = safe_excerpt[:2000] + "\n… (truncated)"
    pre = ""
    if safe_excerpt:
        pre = f"<pre style=\"margin: .75rem 0 0; white-space: pre-wrap;\">{escape_html(safe_excerpt)}</pre>"
    return (
        f"{STALE_BANNER_START}\n"
        "<div style=\"background:#7f1d1d;color:#fff;padding:12px 14px;line-height:1.35;\">"
        "<strong>Warning:</strong> This documentation build is stale/partial because the latest doc build failed. "
        "Some pages may not correspond to the current main branch."
        f"<div style=\"opacity:.9;margin-top:6px\">Commit: <code>{head_sha}</code> (attempted) | "
        f"Time (UTC): <code>{build_time_utc}</code></div>"
        f"{pre}"
        "</div>\n"
        f"{STALE_BANNER_END}\n"
    )


