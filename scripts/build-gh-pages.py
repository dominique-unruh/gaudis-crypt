def load_doc_manifest(doc_dir: Path) -> set[Path]:
    """Read `.lake/build/doc-manifest.json` and return relative doc paths."""
    # doc-gen4 writes a JSON array of strings like "doc/Foo/Bar.html".
    manifest_path = doc_dir.parent / "doc-manifest.json"
    if not manifest_path.exists():
        return set()
    try:
        data = json.loads(read_text(manifest_path))
        out: set[Path] = set()
        for s in data:
            # entries are like "doc/Foo/Bar.html"
            if isinstance(s, str) and s.startswith("doc/"):
                out.add(Path(s[len("doc/") :]))
        return out
    except Exception:
        return set()


def iter_html_files(root_dir: Path) -> list[Path]:
    """Return all .html files under `root_dir` (recursive)."""
    out: list[Path] = []
    if not root_dir.exists():
        return out
    for dp, _, files in os.walk(root_dir):
        for f in files:
            if f.endswith(".html"):
                out.append(Path(dp) / f)
    return out


def reset_docgen_outputs(root: Path) -> None:
    """Remove doc-gen4 build outputs so transitive closure can't go stale.

    doc-gen4 stores its DB and marker files under `.lake/build`. If we change the
    generated entrypoint imports between attempts, those stale DB rows would
    otherwise keep old modules in the transitive closure and we'd regenerate HTML
    for modules that are no longer imported.
    """
    # We do this before each build attempt because doc-gen4's DB is incremental.
    # If we remove imports from the autogenerated entrypoint, we don't want old
    # modules to keep showing up as transitive dependencies.
    build = root / ".lake" / "build"
    # If the project hasn't been built yet, nothing to do.
